public class OperadoresUnarios {
    public static void main(String[] args) {
        int numero = 5;
        numero ++;
        System.out.print(numero);

        int outroNumero = 6;
        outroNumero --;
        System.out.print(outroNumero);

        boolean verdade = true;
        verdade = !verdade;
        System.out.print(verdade);
    }
}
